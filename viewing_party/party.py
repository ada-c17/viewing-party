def create_movie(title, genre, rating):
    movie_dict = {}
    if isinstance(title, str) and isinstance(genre, str) and\
        isinstance(rating, int):
        movie_dict["title"] = title   
        movie_dict["genre"] = genre        
        movie_dict["rating"] = rating
    else: 
        return None    
    return movie_dict

